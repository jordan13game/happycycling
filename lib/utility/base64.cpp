#include "base64.h"

static const string base64_chars = 
             "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
             "abcdefghijklmnopqrstuvwxyz"
             "0123456789+/";

inline bool base64_t::is_base64(char c_)
{
    return (isalnum(c_) || (c_ == '+') || (c_ == '/'));
}             
             
